public class ControlFlowExercises {
    public static void main(String[] args){


        boolean condition = true;
        int i = 5;

        while(condition){
            if(i == 15){
                condition = false;
            }
            System.out.println("this is the while loop: "+ i);
            i++;
        }

        for(int n = 5; n <=15; n++){
            System.out.println("this is the for loop: " + n);
            }






    }
}
